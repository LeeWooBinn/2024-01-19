        System.out.println("1.예금 2.출금 3.잔고 4.종료");
        
        int num = sc.nextInt();
        switch (num) {
        case 1:
	    	System.out.println("예근금액:10000원");
	    	break;
	    case 2:
	    	System.out.println("출금 : 2000원");
	    	break;
	    case 3:
	    	System.out.println("잔고 : 8000원");
	    	break;
	    case 4:
	    	System.out.println("종료 ");
	    	break;
        
	  
        		  
        		
        		
        }
       
        
       
        		
        		
        
        
        
        
		
		

		
